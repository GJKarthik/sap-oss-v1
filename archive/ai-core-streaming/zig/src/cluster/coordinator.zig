test "ClusterTopology constants" {
    try std.testing.expectEqual(@as(u8, 4), ClusterTopology.MAX_NODES);
    try std.testing.expectEqual(@as(u8, 4), ClusterTopology.MAX_GPUS_PER_NODE);
    try std.testing.expectEqual(@as(u8, 16), ClusterTopology.MAX_TOTAL_GPUS);
}

test "NodeInfo hostname" {
    var node = NodeInfo{ .node_id = 0 };
    node.setHostname("worker-0.cluster.local");
    
    try std.testing.expectEqualStrings("worker-0.cluster.local", node.getHostname());
}

test "ClusterCoordinator rank calculations" {
    var coordinator = ClusterCoordinator{
        .allocator = std.testing.allocator,
        .node_id = 2,
        .local_gpu_count = 4,
    };
    
    // Node 2, Local GPU 3 -> Global rank 11
    try std.testing.expectEqual(@as(u8, 11), coordinator.getGlobalRank(3));
    
    // Global rank 11 -> Node 2
    try std.testing.expectEqual(@as(u8, 2), coordinator.getNodeFromRank(11));
    
    // Global rank 11 -> Local GPU 3
    try std.testing.expectEqual(@as(u8, 3), coordinator.getLocalGpuFromRank(11));
    
    // Rank 11 is on node 2
    try std.testing.expect(coordinator.isLocalRank(11));
    
    // Rank 0 is NOT on node 2
    try std.testing.expect(!coordinator.isLocalRank(0));
}

test "ClusterWorkloadDistribution" {
    var coordinator = ClusterCoordinator{
        .allocator = std.testing.allocator,
        .world_size = 2,
        .node_id = 0,
        .local_gpu_count = 4,
    };
    
    // Set up two nodes with 4 GPUs each
    coordinator.nodes[0] = NodeInfo{ .node_id = 0, .gpu_count = 4 };
    coordinator.nodes[1] = NodeInfo{ .node_id = 1, .gpu_count = 4 };
    
    const dist = coordinator.distributeClusterWorkload(100);
    
    // Should have 8 tasks (4 GPUs × 2 nodes)
    try std.testing.expectEqual(@as(u8, 8), dist.task_count);
    
    // Verify total batch size is preserved
    var total: usize = 0;
    for (dist.getTasks()) |maybe_task| {
        if (maybe_task) |task| {
            total += task.batch_size;
        }
    }
    try std.testing.expectEqual(@as(usize, 100), total);
}