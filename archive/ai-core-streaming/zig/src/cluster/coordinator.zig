pub const ClusterTask = struct {
    /// Global GPU rank (0-15)
    global_rank: u8,
    /// Node ID (0-3)
    node_id: u8,
    /// Local GPU ID within node (0-3)
    local_gpu: u8,
    /// Start index in batch
    batch_start: usize,
    /// Number of items for this GPU
    batch_size: usize,
    /// Completion status
    completed: bool = false,
};

pub const ClusterWorkloadDistribution = struct {
    tasks: [ClusterTopology.MAX_TOTAL_GPUS]?ClusterTask = 
        [_]?ClusterTask{null} ** ClusterTopology.MAX_TOTAL_GPUS,
    task_count: u8 = 0,
    total_batch_size: usize = 0,
    
    pub fn getTasks(self: *const ClusterWorkloadDistribution) []const ?ClusterTask {
        return self.tasks[0..self.task_count];
    }
    
    /// Get task for a specific global rank
    pub fn getTask(self: *const ClusterWorkloadDistribution, global_rank: u8) ?ClusterTask {
        if (global_rank >= ClusterTopology.MAX_TOTAL_GPUS) return null;
        return self.tasks[global_rank];
    }
};

// ============================================================================
// Ring AllReduce for Network-Efficient Gradient Sync
// ============================================================================

pub const RingAllReduce = struct {
    coordinator: *ClusterCoordinator,
    
    const Self = @This();
    
    pub fn init(coordinator: *ClusterCoordinator) Self {
        return .{ .coordinator = coordinator };
    }
    
    /// Perform ring AllReduce across cluster
    /// This is bandwidth-optimal for large tensors over network
    pub fn reduce(
        self: *Self,
        local_gpu: u8,
        data: []f32,
    ) !void {
        const total_gpus = self.coordinator.getTotalGpuCount();
        if (total_gpus <= 1) return; // Nothing to reduce
        
        // Ring AllReduce algorithm:
        // 1. Split data into `total_gpus` chunks
        // 2. Each GPU sends chunk[i] to GPU[(rank+1) % total] and receives into chunk[(rank-1) % total]
        // 3. Repeat `total_gpus - 1` times for reduce-scatter phase
        // 4. Repeat `total_gpus - 1` times for all-gather phase
        
        // For NCCL, this is handled internally with optimized implementation
        if (self.coordinator.nccl_comm) |comm| {
            const data_ptr: *anyopaque = @ptrCast(data.ptr);
            try comm.allReduce(
                local_gpu,
                data_ptr,
                data_ptr,
                data.len,
                .ncclFloat32,
                .ncclSum,
            );
        }
        
        log.debug("Ring AllReduce completed on GPU {} with {} elements", .{ local_gpu, data.len });
    }
};

