#!/usr/bin/env python3
"""
add_spdx_headers.py — Prepend SPDX-License-Identifier + SPDX-FileCopyrightText to
every source file in SAP-owned services that does not already have them.

Rules:
  - Idempotent: files already carrying SPDX headers are untouched.
  - Shebang-aware: #!/usr/bin/env ... line stays as line 1.
  - Skips generated files (DO NOT EDIT, Code generated, AUTO-GENERATED).
  - Skips vendor / node_modules / dist / build / target / .venv directories.
  - Does NOT touch: mangle-main (Google LLC copyright already present →
    covered by REUSE.toml), vllm-main, elasticsearch-main (upstream OSS),
    ai-core-pal / ai-core-streaming (empty stubs).

Exit codes:
  0 — success
  1 — at least one file could not be updated

Usage:
  # Preview changes without writing anything
  python3 scripts/sbom-lineage/add_spdx_headers.py --dry-run

  # Apply to all services
  python3 scripts/sbom-lineage/add_spdx_headers.py

  # Apply to a single service
  python3 scripts/sbom-lineage/add_spdx_headers.py --service cap-llm-plugin-main
"""
