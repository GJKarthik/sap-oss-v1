const CliArgs = struct {
    model: ?[]const u8 = null,
    variant: ?[]const u8 = null,
    hardware: []const u8 = "t4",
    task: ?[]const u8 = null,
    format: enum { json, yaml } = .json,
    mangle_path: []const u8 = "./mangle",
    output: ?[]const u8 = null,
    list_models: bool = false,
};

pub fn main() !void {
    var gpa = std.heap.GeneralPurposeAllocator(.{}){};
    defer _ = gpa.deinit();
    const allocator = gpa.allocator();

    const args = try parseArgs(allocator);

    // Initialize Mangle query interface
    var query = MangleConfigQuery.init(allocator, args.mangle_path) catch |err| {
        std.log.warn("Could not initialize Mangle query (using defaults): {}", .{err});
        // Continue with defaults
        try generateDefaultConfig(allocator, args);
        return;
    };
    defer query.deinit();

    if (args.list_models) {
        try listDeployableModels(allocator, &query, args.hardware);
        return;
    }

    // Generate config
    const config = if (args.model) |model|
        try query.queryDeploymentConfig(model, args.variant, args.hardware)
    else if (args.task) |task|
        try query.queryRecommendedDeployment(task, args.hardware)
    else {
        std.log.err("Must specify --model or --task", .{});
        return error.MissingArgument;
    };

    // Output
    const output = switch (args.format) {
        .json => try config.toJson(allocator),
        .yaml => try config.toYaml(allocator),
    };
    defer allocator.free(output);

    if (args.output) |path| {
        const file = try std.fs.cwd().createFile(path, .{});
        defer file.close();
        try file.writeAll(output);
        std.log.info("Config written to {s}", .{path});
    } else {
        const stdout = std.io.getStdOut().writer();
        try stdout.writeAll(output);
        try stdout.writeAll("\n");
    }
}

fn parseArgs(allocator: Allocator) !CliArgs {
    _ = allocator;
    var args = CliArgs{};

    var arg_iter = std.process.args();
    _ = arg_iter.skip(); // Skip program name

    while (arg_iter.next()) |arg| {
        if (std.mem.eql(u8, arg, "--model")) {
            args.model = arg_iter.next();
        } else if (std.mem.eql(u8, arg, "--variant")) {
            args.variant = arg_iter.next();
        } else if (std.mem.eql(u8, arg, "--hardware")) {
            args.hardware = arg_iter.next() orelse "t4";
        } else if (std.mem.eql(u8, arg, "--task")) {
            args.task = arg_iter.next();
        } else if (std.mem.eql(u8, arg, "--format")) {
            const fmt = arg_iter.next() orelse "json";
            args.format = if (std.mem.eql(u8, fmt, "yaml")) .yaml else .json;
        } else if (std.mem.eql(u8, arg, "--mangle-path")) {
            args.mangle_path = arg_iter.next() orelse "./mangle";
        } else if (std.mem.eql(u8, arg, "--output") or std.mem.eql(u8, arg, "-o")) {
            args.output = arg_iter.next();
        } else if (std.mem.eql(u8, arg, "--list-models")) {
            args.list_models = true;
        } else if (std.mem.eql(u8, arg, "--help") or std.mem.eql(u8, arg, "-h")) {
            printHelp();
            std.process.exit(0);
        }
    }

    return args;
}

