pub const MangleConfigQuery = struct {
    allocator: Allocator,
    db: *deductive_db.DeductiveDB,

    const Self = @This();

    pub fn init(allocator: Allocator, mangle_path: []const u8) !Self {
        var db = try deductive_db.DeductiveDB.init(allocator);

        // Load Mangle rule files
        const rule_files = [_][]const u8{
            "model_store_rules.mg",
            "batching_rules.mg",
            "aicore_deployment.mg",
        };

        for (rule_files) |file| {
            const full_path = try std.fs.path.join(allocator, &.{ mangle_path, file });
            defer allocator.free(full_path);
            db.loadRulesFromFile(full_path) catch |err| {
                std.log.warn("Could not load {s}: {}", .{ file, err });
            };
        }

        return Self{
            .allocator = allocator,
            .db = db,
        };
    }

    pub fn deinit(self: *Self) void {
        self.db.deinit();
    }

    /// Query deployment config for a specific model and hardware
    pub fn queryDeploymentConfig(
        self: *Self,
        repo_id: []const u8,
        variant_file: ?[]const u8,
        hardware_profile: []const u8,
    ) !AICoreConfig {
        // Query Mangle rules for configuration parameters
        const resource_plan = try self.queryResourcePlan(hardware_profile);
        const scaling = try self.queryScaling(repo_id, variant_file);
        const context_size = try self.queryContextSize(repo_id, variant_file, hardware_profile);
        const parallel = try self.queryParallel(repo_id, hardware_profile);
        const docker_image = try self.queryDockerImage(resource_plan);
        const artifact_url = try self.queryArtifactUrl(repo_id, variant_file);

        return AICoreConfig{
            .scenario = "ainuc-llm-inference",
            .executable = "llm-server",
            .model = repo_id,
            .variant = variant_file,
            .resource_plan = resource_plan,
            .min_replicas = scaling.min,
            .max_replicas = scaling.max,
            .context_size = context_size,
            .parallel_requests = parallel,
            .docker_image = docker_image,
            .model_file = variant_file orelse "model.safetensors",
            .artifact_url = artifact_url,
            .mangle_enabled = true,
        };
    }

    /// Query for recommended deployment based on task type
    pub fn queryRecommendedDeployment(
        self: *Self,
        task: []const u8,
        hardware_profile: []const u8,
    ) !AICoreConfig {
        // Query route_request to get model for task
        const model = try self.queryRouteRequest(task);

        // Check if model has GGUF variants
        const variant = self.queryBestVariant(model, hardware_profile) catch null;

        return self.queryDeploymentConfig(model, variant, hardware_profile);
    }

    // Internal query methods
    fn queryResourcePlan(self: *Self, hardware_profile: []const u8) ![]const u8 {
        // Query: aicore_resource_plan(HardwareProfile, Plan)
        const query = try std.fmt.allocPrint(
            self.allocator,
            "aicore_resource_plan(\"{s}\", Plan)",
            .{hardware_profile},
        );
        defer self.allocator.free(query);

        if (self.db.query(query)) |result| {
            return result.get("Plan") orelse "gpu_nvidia_t4";
        }
        return "gpu_nvidia_t4"; // Default
    }

    fn queryScaling(self: *Self, repo_id: []const u8, variant_file: ?[]const u8) !struct { min: u32, max: u32 } {
        // Query: aicore_scaling(RepoId, Min, Max) or aicore_scaling_variant(...)
        _ = repo_id;
        _ = variant_file;

        // For now, use size-based heuristics until full Mangle integration
        // This would normally query scaling_for_size rules
        return .{ .min = 2, .max = 4 };
    }

    fn queryContextSize(self: *Self, repo_id: []const u8, variant_file: ?[]const u8, hardware_profile: []const u8) !u32 {
        // Query: aicore_context(RepoId, HardwareProfile, ContextSize)
        _ = self;
        _ = repo_id;
        _ = variant_file;
        _ = hardware_profile;

        // Default based on common T4 config
        return 4096;
    }

    fn queryParallel(self: *Self, repo_id: []const u8, hardware_profile: []const u8) !u32 {
        // Query: aicore_parallel(RepoId, HardwareProfile, Parallel)
        _ = self;
        _ = repo_id;
        _ = hardware_profile;

        return 4; // Default for T4
    }

    fn queryDockerImage(self: *Self, resource_plan: []const u8) ![]const u8 {
        // Query: docker_image_for_plan(Plan, Image)
        _ = self;

        if (std.mem.startsWith(u8, resource_plan, "gpu_")) {
            return "ainuc-llm-server:cuda-latest";
        } else {
            return "ainuc-llm-server:cpu-latest";
        }
    }

    fn queryArtifactUrl(self: *Self, repo_id: []const u8, variant_file: ?[]const u8) ![]const u8 {
        // Query: aicore_artifact(RepoId, Artifact) or aicore_artifact_variant(...)
        _ = self;

        if (variant_file) |vf| {
            return try std.fmt.allocPrint(
                self.allocator,
                "ai://default/llm-models/gguf/{s}",
                .{vf},
            );
        } else {
            const safe_name = sanitizeForPath(repo_id);
            return try std.fmt.allocPrint(
                self.allocator,
                "ai://default/llm-models/safetensors/{s}",
                .{safe_name},
            );
        }
    }

    fn queryRouteRequest(self: *Self, task: []const u8) ![]const u8 {
        // Query: route_request(Task, Model)
        _ = self;

        // Default routing based on task type
        if (std.mem.eql(u8, task, "chat") or std.mem.eql(u8, task, "completion")) {
            return "TheBloke/Mistral-7B-Instruct-v0.2-GGUF";
        } else if (std.mem.eql(u8, task, "embedding")) {
            return "sentence-transformers/all-MiniLM-L6-v2";
        } else if (std.mem.eql(u8, task, "reasoning")) {
            return "microsoft/phi-2";
        }
        return "TheBloke/Mistral-7B-Instruct-v0.2-GGUF";
    }

    fn queryBestVariant(self: *Self, repo_id: []const u8, hardware_profile: []const u8) ![]const u8 {
        // Query: best_variant_for_hardware(RepoId, HardwareProfile, VariantFile)
        _ = self;
        _ = hardware_profile;

        // Return common variant based on repo
        if (std.mem.indexOf(u8, repo_id, "Mistral-7B") != null) {
            return "mistral-7b-instruct-v0.2.Q4_K_M.gguf";
        } else if (std.mem.indexOf(u8, repo_id, "Llama-2-7B") != null) {
            return "llama-2-7b.Q4_K_M.gguf";
        }
        return error.NoVariantFound;
    }
};

