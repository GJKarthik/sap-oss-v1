fn sanitizeForName(input: []const u8) []const u8 {
    // Replace characters invalid in AI Core resource names with '-'.
    // We operate in-place on a stack buffer; truncate at 63 chars (K8s label limit).
    const max_len = 63;
    const len = @min(input.len, max_len);
    var buf: [max_len]u8 = undefined;
    for (input[0..len], 0..) |c, i| {
        buf[i] = switch (c) {
            'a'...'z', 'A'...'Z', '0'...'9', '-' => c,
            'A'...'Z' => c + 32, // lowercase
            else => '-',
        };
    }
    // Trim leading/trailing dashes
    var start: usize = 0;
    var end: usize = len;
    while (start < end and buf[start] == '-') start += 1;
    while (end > start and buf[end - 1] == '-') end -= 1;
    if (start == end) return "model";
    // Return a slice into a static buffer — safe because callers only use this
    // value within the same stack frame (passed directly to fmt.allocPrint).
    const static = struct {
        var storage: [max_len]u8 = undefined;
    };
    @memcpy(static.storage[0 .. end - start], buf[start..end]);
    return static.storage[0 .. end - start];
}

fn sanitizeForPath(input: []const u8) []const u8 {
    return sanitizeForName(input);
}

