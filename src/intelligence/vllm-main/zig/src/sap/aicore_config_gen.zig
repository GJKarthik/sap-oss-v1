//! AI Core Configuration Generator
//!
//! Generates SAP BTP AI Core deployment configurations by querying Mangle rules.
//! This replaces hardcoded JSON/YAML configs with deductive rule-driven configs.
//!
//! Usage:
//!   ./aicore_config_gen --model "TheBloke/Mistral-7B-Instruct-v0.2-GGUF" --hardware t4
//!   ./aicore_config_gen --task chat --hardware t4 --format yaml

const std = @import("std");
const Allocator = std.mem.Allocator;
const mangle = @import("mangle.zig");
const deductive_db = @import("deductive_db.zig");

// ============================================================================
// AI Core Configuration Types
// ============================================================================

pub const AICoreConfig = struct {
    scenario: []const u8,
    executable: []const u8,
    model: []const u8,
    variant: ?[]const u8,
    resource_plan: []const u8,
    min_replicas: u32,
    max_replicas: u32,
    context_size: u32,
    parallel_requests: u32,
    docker_image: []const u8,
    model_file: []const u8,
    artifact_url: []const u8,
    mangle_enabled: bool,

    pub fn toJson(self: *const AICoreConfig, allocator: Allocator) ![]const u8 {
        var buffer = std.ArrayList(u8){};
        const writer = buffer.writer();

        try writer.print(
            \\{{
            \\  "name": "{s}-{s}",
            \\  "scenarioId": "{s}",
            \\  "executableId": "{s}",
            \\  "parameterBindings": [
            \\    {{"key": "dockerImage", "value": "{s}"}},
            \\    {{"key": "resourcePlan", "value": "{s}"}},
            \\    {{"key": "modelFile", "value": "{s}"}},
            \\    {{"key": "contextSize", "value": "{d}"}},
            \\    {{"key": "parallelRequests", "value": "{d}"}},
            \\    {{"key": "minReplicas", "value": "{d}"}},
            \\    {{"key": "maxReplicas", "value": "{d}"}},
            \\    {{"key": "mangleEnabled", "value": "{s}"}}
            \\  ],
            \\  "inputArtifactBindings": [
            \\    {{"key": "llm-model", "artifactId": "ARTIFACT_ID_PLACEHOLDER"}}
            \\  ]
            \\}}
        , .{
            self.scenario,
            sanitizeForName(self.model),
            self.scenario,
            self.executable,
            self.docker_image,
            self.resource_plan,
            self.model_file,
            self.context_size,
            self.parallel_requests,
            self.min_replicas,
            self.max_replicas,
            if (self.mangle_enabled) "true" else "false",
        });

        return buffer.toOwnedSlice();
    }

    pub fn toYaml(self: *const AICoreConfig, allocator: Allocator) ![]const u8 {
        var buffer = std.ArrayList(u8){};
        const writer = buffer.writer();

        try writer.print(
            \\# Generated by aicore_config_gen from Mangle rules
            \\# Model: {s}
            \\# Hardware: derived from resource_plan
            \\apiVersion: ai.sap.com/v1alpha1
            \\kind: Configuration
            \\metadata:
            \\  name: {s}-config
            \\  labels:
            \\    scenarios.ai.sap.com/id: {s}
            \\spec:
            \\  scenarioId: {s}
            \\  executableId: {s}
            \\  parameterBindings:
            \\    - key: dockerImage
            \\      value: "{s}"
            \\    - key: resourcePlan
            \\      value: "{s}"
            \\    - key: modelFile
            \\      value: "{s}"
            \\    - key: contextSize
            \\      value: "{d}"
            \\    - key: parallelRequests
            \\      value: "{d}"
            \\    - key: minReplicas
            \\      value: "{d}"
            \\    - key: maxReplicas
            \\      value: "{d}"
            \\    - key: mangleEnabled
            \\      value: "{s}"
            \\  inputArtifactBindings:
            \\    - key: llm-model
            \\      artifactId: "{{{{ ARTIFACT_ID }}}}"
        , .{
            self.model,
            sanitizeForName(self.model),
            self.scenario,
            self.scenario,
            self.executable,
            self.docker_image,
            self.resource_plan,
            self.model_file,
            self.context_size,
            self.parallel_requests,
            self.min_replicas,
            self.max_replicas,
            if (self.mangle_enabled) "true" else "false",
        });

        return buffer.toOwnedSlice();
    }
};

pub const ArtifactConfig = struct {
    name: []const u8,
    kind: []const u8,
    url: []const u8,
    scenario_id: []const u8,
    labels: std.StringHashMap([]const u8),

    pub fn toJson(self: *const ArtifactConfig, allocator: Allocator) ![]const u8 {
        var buffer = std.ArrayList(u8){};
        const writer = buffer.writer();

        try writer.print(
            \\{{
            \\  "name": "{s}",
            \\  "kind": "{s}",
            \\  "url": "{s}",
            \\  "scenarioId": "{s}",
            \\  "labels": [
        , .{
            self.name,
            self.kind,
            self.url,
            self.scenario_id,
        });

        var first = true;
        var iter = self.labels.iterator();
        while (iter.next()) |entry| {
            if (!first) try writer.writeAll(",");
            try writer.print(
                \\
                \\    {{"key": "{s}", "value": "{s}"}}
            , .{ entry.key_ptr.*, entry.value_ptr.* });
            first = false;
        }

        try writer.writeAll(
            \\
            \\  ]
            \\}
        );

        return buffer.toOwnedSlice();
    }
};

