fn generateDefaultConfig(allocator: Allocator, args: CliArgs) !void {
    // Generate a default config when Mangle is not available
    const config = AICoreConfig{
        .scenario = "ainuc-llm-inference",
        .executable = "llm-server",
        .model = args.model orelse "TheBloke/Mistral-7B-Instruct-v0.2-GGUF",
        .variant = args.variant,
        .resource_plan = if (std.mem.eql(u8, args.hardware, "t4")) "gpu_nvidia_t4" else "infer.l",
        .min_replicas = 2,
        .max_replicas = 4,
        .context_size = 4096,
        .parallel_requests = 4,
        .docker_image = "ainuc-llm-server:cuda-latest",
        .model_file = args.variant orelse "mistral-7b-instruct-v0.2.Q4_K_M.gguf",
        .artifact_url = "ai://default/llm-models/gguf/",
        .mangle_enabled = true,
    };

    const output = switch (args.format) {
        .json => try config.toJson(allocator),
        .yaml => try config.toYaml(allocator),
    };
    defer allocator.free(output);

    const stdout = std.io.getStdOut().writer();
    try stdout.writeAll(output);
    try stdout.writeAll("\n");
}

